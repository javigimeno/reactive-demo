package com.baeldung.javier.firstwork.reactive.demo.models;

public class Foo {
    Long id;
    String name;

    public Foo(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
